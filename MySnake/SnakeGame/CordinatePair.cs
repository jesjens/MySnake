
namespace MySnake.SnakeGame
{
    public class CordinatePair
    {
        public int X { get; set; }
        public int Y { get; set; }

        public CordinatePair()
        {

        }

        public CordinatePair(int x, int y)
        {
            this.X = x;
            this.Y = y;
        }
    }
}
